<?php
/*
    - São uma forma que temos para declarar um valor e salvá-lo na memória;
    - Uma variável em PHP tem o $ na frente do seu nome;
    - Ex: $nome = "Jose"
    - Podemos salvar qualquer tipo de dado;
    - Podemos alterar o valor de uma variável no decorrer do programa;
    - Podemos imprimir o valor de uma variável com echo;
*/
$teste = "Algum texto";

echo $teste;
echo "\n";

$num = 10;
echo $num;
echo "\n";

$_nome = "Juvenal";
echo $_nome;
echo "\n";

#Utilização de camel case
$velocidadeMaxima = 80;
#Ou
$velocidade_minima = 30;

?>