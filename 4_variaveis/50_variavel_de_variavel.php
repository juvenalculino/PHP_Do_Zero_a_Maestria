<?php
/*
    - Podemos criar uma variável por meio do nome de outra variável, com um valor diferente;
    - O símbolo para esta função é o $$
    - Ex: $x = "teste";
          $$x = 5;
    - Após a execução do código, a variável teste (conteúdo de $x), será criada com valor 5.
*/
$x = 'nome'; // Valor de nom  e
echo "$x <br>";

$$x = 'Juvenal'; // Var com o nome de x (nome), com valor de juvenal

echo "$nome <br>";
?>

