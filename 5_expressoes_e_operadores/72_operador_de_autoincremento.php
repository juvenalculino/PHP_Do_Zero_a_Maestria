<?php
/*
    - Podemos incrementar um valor ou decrementar o valor com os operadores: ++ e --;
    - Ex: $n++ ou $n--
    - Onde n e x são variáveis, e terão seus valores alterador com +1 e -1;
    - Estes operadores são muitos utilizados em estrutura de rpetição;
*/
$x = 0;
$y = 0;

$x++;
$y = $y + 1;

echo "x " . $x . "<br>";
echo "y " . $y . "<br>";

$n = 10;
$m = 10;

$n--;
$m = $m - 1;

echo "n " . $n . "<br>";
echo "m " . $m . "<br>";
